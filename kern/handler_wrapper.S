#define DEFINE_HANDLER_WRAPPER(interrupt, operation) \
    .global wrap_handler##interrupt; \
    wrap_handler##interrupt: \
        operation(); \
        pushal; \
        pushl %gs; \
        pushl %fs; \
        pushl %es; \
        pushl %ds; \
        movl %cr2, %eax; \
        pushl %eax; \
        pushl $interrupt; \
         \
        movl $24, %eax; \
        movl %eax, %ds; \
        movl %eax, %es; \
        movl %eax, %fs; \
        movl %eax, %gs; \
         \
        pushl %esp; \
        call handle; \
        addl $4, %esp; \
         \
        addl $8, %esp; \
        popl %ds; \
        popl %es; \
        popl %fs; \
        popl %gs; \
        popal; \
        addl $4, %esp; \
        iret

.global wrap_user_provided_handler
wrap_user_provided_handler:
    pushl %ebp
    movl %esp, %ebp
    pushl %ebx
    pushl %esi
    pushl %edi

    // store %ds, %es, %fs, %gs before changing them
    pushl %ds
    pushl %es
    pushl %fs
    pushl %gs

    // fetch arguemnts
    movl 8(%ebp), %eax // ureg_ptr
    movl 12(%ebp), %edx // exception_stack
    movl 16(%ebp), %ecx // handler
    movl 20(%ebp), %ebx // arg

    // switch to user provided exception stack
    movl %esp, %esi
    movl %edx, %esp
    pushl %esi

    // copy ureg
    movl 76(%eax), %esi // ureg_ptr->ss
    pushl %esi
    movl 72(%eax), %esi // ureg_ptr->esp
    pushl %esi
    movl 68(%eax), %esi // ureg_ptr->eflags
    pushl %esi
    movl 64(%eax), %esi // ureg_ptr->cs
    pushl %esi
    movl 60(%eax), %esi // ureg_ptr->eip
    pushl %esi
    movl 56(%eax), %esi // ureg_ptr->error_code
    pushl %esi
    movl 52(%eax), %esi // ureg_ptr->eax
    pushl %esi
    movl 48(%eax), %esi // ureg_ptr->ecx
    pushl %esi
    movl 44(%eax), %esi // ureg_ptr->edx
    pushl %esi
    movl 40(%eax), %esi // ureg_ptr->ebx
    pushl %esi
    movl 36(%eax), %esi // ureg_ptr->zero
    pushl %esi
    movl 32(%eax), %esi // ureg_ptr->ebp
    pushl %esi
    movl 28(%eax), %esi // ureg_ptr->esi
    pushl %esi
    movl 24(%eax), %esi // ureg_ptr->edi
    pushl %esi
    movl 20(%eax), %esi // ureg_ptr->gs
    pushl %esi
    movl 16(%eax), %esi // ureg_ptr->fs
    pushl %esi
    movl 12(%eax), %esi // ureg_ptr->es
    pushl %esi
    movl 8(%eax), %esi // ureg_ptr->ds
    pushl %esi
    movl 4(%eax), %esi // ureg_ptr->cr2
    pushl %esi
    movl (%eax), %esi // urge_ptr->cause
    pushl %esi

    // change %ds, %es, %fs, %gs
    movl $43, %esi
    movl %esi, %ds
    movl %esi, %es
    movl %esi, %fs
    movl %esi, %gs

    // call handler
    movl %esp, %esi
    movl %ecx, %edi
    pushl %eax
    pushl %edx
    pushl %ecx
    movl $0, %eax
    movl $0, %edx
    movl $0, %ecx
    pushl %esi
    pushl %ebx
    pushl $0
    jmp *%edi
    addl $8, %esp
    popl %ecx
    popl %edx
    popl %eax

    // switch back to kernel stack
    addl $80, %esp
    popl %esp

    // restore %ds, %es, %fs, %gs
    popl %gs
    popl %fs
    popl %es
    popl %ds

    movl -12(%ebp), %edi
    movl -8(%ebp), %esi
    movl -4(%ebp), %ebx
    movl %ebp, %esp     
    popl %ebp
    ret

